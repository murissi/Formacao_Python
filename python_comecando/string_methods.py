texto = "karim Benzema is the first Real Madrid"

# capitalize() - Primeira letra do texto Ã© maiscula
print(texto.capitalize())

# casefold() - Todas os caracteres em lowercase
print(texto.casefold())

# center() - centraliza o text com caracter especifico
centralizado = "Hello".center(11, "-")
